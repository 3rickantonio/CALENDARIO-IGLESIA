<h4>Mi tiempo es para Dios</h4>

<p>-----------------------------------------------------------</p2>
<p>Todo tiene su tiempo, y todo lo que se quiere debajo del cielo tiene su hora.</p>
<p>2 Tiempo de nacer, y tiempo de morir; tiempo de plantar, y tiempo de arrancar lo plantado;</p>
<p>3 tiempo de matar, y tiempo de curar; tiempo de destruir, y tiempo de edificar;</p>
<p>4 tiempo de llorar, y tiempo de reír; tiempo de endechar, y tiempo de bailar;</p>
<p>5 tiempo de esparcir piedras, y tiempo de juntar piedras; tiempo de abrazar, y tiempo de abstenerse de abrazar;</p>
<p>6 tiempo de buscar, y tiempo de perder; tiempo de guardar, y tiempo de desechar;</p>
<p>7 tiempo de romper, y tiempo de coser; tiempo de callar, y tiempo de hablar;</p>
<p>8 tiempo de amar, y tiempo de aborrecer; tiempo de guerra, y tiempo de paz.</p>

	<p><strong>Eclesiastés 3 Reina-Valera 1960 (RVR1960)</strong></p>
